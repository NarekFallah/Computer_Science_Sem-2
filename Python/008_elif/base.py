n1=int(input("Please Enter Your First Number "))
Op=str(input("Please Enter Your Operator "))
n2=int(input("Please Enter Your Second Number "))
Op1= str("-")
Op2= str("+")
Op3= str("*")
Op4= str("/")
if Op == Op1 :
    print(n1-n2)
elif Op == Op2 :
    print(n1+n2)
elif Op == Op3 :
    print(n1*n2)
elif Op == Op4 :
    print(n1/n2)


